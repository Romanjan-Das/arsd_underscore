window.onload=function(){
    var blank_div_1 = document.createElement("div");
    blank_div_1.id="left_blank";
    var blank_div_2 = document.createElement("div");
    blank_div_2.id="right_blank";
    var page = document.getElementById("page");
    page.appendChild(blank_div_1);
    page.appendChild(blank_div_2);
}